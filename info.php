					</p><br />
					<p>
						<strong>Water Containers</strong> - There is delicious fresh mountain water available on site near the stages so bring a refillable non-glass container to minimize waste.  Be sure to have a container for water for your campsite as it could be a bit of a stroll if you wake up thirsty.
					</p><br />
					<p>
