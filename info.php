					</p>
					<br />
					<p>
						<strong>Clothing and costumery for all weather</strong> - It's a good idea to bring some warm nighttime clothes as well as 'non-clothes' for fun in the daytime sun.  Though it's usually quite warm this time of year in Belden, with all the crazy weather we've seen lately you just never know for sure.  Don't forget, music goes 24/7 and it's always coldest right before dawn!
					</p><br />
					<p>
						<strong>Food</strong> - Belden Town has a restaurant and Convergence will be serving delicious, nutritious hot meals, hooray!  As well, coffee will be sold by Cafe Mekka and Smoothie Operators will be whipping up cool tasty treats for us.  That said it is always a good idea to bring a few snacks to keep you going.  You may bring a small camp-stove, so long as it's not a full on BBQ grill.  No charcoal grills or open flames are allowed.
					</p><br />
					<p>
